#!/usr/bin/env node
/**
 * ensure-precommit-hook.mjs
 *
 * Install the git pre-commit hook defined by <repo-root>/.pre-commit-config.yaml
 * (Ruff for the Python backends, ESLint + Prettier for the WebUI). Run as part of
 * `npm run setup` so a fresh clone is ready to commit without a manual step.
 *
 * Best effort and idempotent:
 *   - No-op if this is not a git checkout (e.g. a source tarball).
 *   - No-op if the hook is already installed.
 *   - Uses `pre-commit` from PATH, else falls back to `uvx pre-commit`
 *     (uv ships with this repo's toolchain). If neither is available it prints
 *     how to install it and exits 0 — never blocks setup.
 */
import { spawnSync } from 'node:child_process'
import { existsSync, readFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const webuiDir = path.resolve(fileURLToPath(new URL('../../', import.meta.url)))
const repoRoot = path.resolve(webuiDir, '..')

function log(msg) {
  console.log(`[ensure-precommit-hook] ${msg}`)
}

// Resolve a path relative to the git dir (handles worktrees and `.git` files).
function gitPath(relative) {
  const res = spawnSync('git', ['rev-parse', '--git-path', relative], {
    cwd: repoRoot,
    encoding: 'utf-8',
  })
  if (res.status !== 0) return undefined
  const out = res.stdout.trim()
  return path.isAbsolute(out) ? out : path.join(repoRoot, out)
}

// Skip cleanly when this is not a git working tree.
if (
  spawnSync('git', ['rev-parse', '--is-inside-work-tree'], { cwd: repoRoot, stdio: 'ignore' })
    .status !== 0
) {
  log('Not a git checkout — skipping pre-commit hook install.')
  process.exit(0)
}

const configPath = path.join(repoRoot, '.pre-commit-config.yaml')
if (!existsSync(configPath)) {
  log(`No .pre-commit-config.yaml at ${configPath} — skipping.`)
  process.exit(0)
}

// Already installed? The shim pre-commit writes carries a recognizable marker.
const hookPath = gitPath('hooks/pre-commit')
if (hookPath && existsSync(hookPath)) {
  try {
    if (readFileSync(hookPath, 'utf-8').includes('File generated by pre-commit')) {
      log('pre-commit hook already installed — nothing to do.')
      process.exit(0)
    }
  } catch {
    /* fall through and (re)install */
  }
}

function commandAvailable(cmd, args = ['--version']) {
  return spawnSync(cmd, args, { stdio: 'ignore' }).status === 0
}

// Prefer a `pre-commit` on PATH; otherwise run it through uv's `uvx`.
let runner
if (commandAvailable('pre-commit')) {
  runner = ['pre-commit']
} else if (commandAvailable('uvx')) {
  runner = ['uvx', 'pre-commit']
} else {
  log('`pre-commit` is not installed and `uvx` is unavailable, so the hook was not installed.')
  log('Install it once with either of:')
  log('    uv tool install pre-commit')
  log('    pipx install pre-commit')
  log('then re-run `npm run setup` (or `pre-commit install`).')
  process.exit(0)
}

log(`Installing git pre-commit hook with: ${runner.join(' ')} install`)
const res = spawnSync(runner[0], [...runner.slice(1), 'install'], {
  cwd: repoRoot,
  stdio: 'inherit',
})
if (res.status !== 0) {
  log('Could not install the pre-commit hook (continuing anyway).')
  log('You can install it manually later with `pre-commit install`.')
  process.exit(0)
}

log('pre-commit hook installed.')
